/**
 * Wearable item represents items that can be worn by the player character, such as clothing or accessories.
 * Extends the Item class.
 *
 * @author Christy Yang
 */
public class Wearable extends Item {
    /**
     * Constructs a new Wearable with the given name.
     *
     * @param name the name of the wearable item
     */
    public Wearable(String name) {
        super(name);
    }
}



